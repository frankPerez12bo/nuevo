<h1 style="text-align: center;cursor: pointer;">Etiquetas HTML</h1>
<p> Este ejemplo muestra cómo combinar algunas de las etiquetas más básicas de HTML5. <br>
Recuerda que <strong>es importante entender la diferencias entre ellas</strong>.
</p>
<h2>Etiqueta ul+li</h2>
<p>
Si listamos personas nominadas a los Oscars, dado que el orden no altera el significado, debemos usar <em>ul</em>.
</p>
<ul>
    <li>José Luis Perales</li>
    <li>José José</li>
    <li>Isabel Pantoja</li>
    <li>Daniela Romo</li>
</ul>
<h2>Etiqueta ol+li</h2>
<p>
En el caso de que estemos listando elementos donde el orden es importante, como por ejemplo la clasificación de un mundial de fútbol, debemos
usar <em>ol</em>.
</p>
<ol>
    <li>Brasil</li>
    <li>Argentina</li>
    <li>Colombia</li>
    <li>Chile</li>
</ol>
